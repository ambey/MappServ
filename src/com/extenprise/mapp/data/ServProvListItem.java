package com.extenprise.mapp.data;

public class ServProvListItem {
    private String firstName;
    private String lastName;
    private String speciality;
    private float experience;
    private String servPtName;
    private String servPtLoaction;

    public String getFirstName() {
        return firstName;
    }

    public void setFirstName(String firstName) {
        this.firstName = firstName;
    }

    public String getLastName() {
        return lastName;
    }

    public void setLastName(String lastName) {
        this.lastName = lastName;
    }

    public String getSpeciality() {
        return speciality;
    }

    public void setSpeciality(String speciality) {
        this.speciality = speciality;
    }

    public float getExperience() {
        return experience;
    }

    public void setExperience(float experience) {
        this.experience = experience;
    }

    public String getServPtName() {
        return servPtName;
    }

    public void setServPtName(String servPtName) {
        this.servPtName = servPtName;
    }

    public String getServPtLoaction() {
        return servPtLoaction;
    }

    public void setServPtLoaction(String servPtLoaction) {
        this.servPtLoaction = servPtLoaction;
    }
}
